#include "transform.h"

t_matrix	translate(double x, double y, double z)
{
	return ((t_matrix){.size = 4, .values = {
			{1, 0, 0, x},
			{0, 1, 0, y},
			{0, 0, 1, z},
			{0, 0, 0, 1}}});
}
