#include "transform.h"

t_matrix	rotatey(double r)
{
	return ((t_matrix){.size = 4, .values = {
		{cos(r), 0, sin(r), 0},
		{0, 1, 0, 0},
		{-sin(r), 0, cos(r), 0},
		{0, 0, 0, 1}}});
}
