#include "transform.h"

t_matrix	shearing(t_shearing p)
{
	return ((t_matrix){.size = 4, .values = {
		{1, p.xy, p.xz, 0},
		{p.yx, 1, p.yz, 0},
		{p.zx, p.zy, 1, 0},
		{0, 0, 0, 1}}});
}
