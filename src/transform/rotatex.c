#include "transform.h"

t_matrix	rotatex(double r)
{
	return ((t_matrix){.size = 4, .values = {
		{1, 0, 0, 0},
		{0, cos(r), -sin(r), 0},
		{0, sin(r), cos(r), 0},
		{0, 0, 0, 1}}});
}
