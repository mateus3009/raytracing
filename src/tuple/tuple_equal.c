#include "tuple.h"

bool	tuple_equal(t_tuple a, t_tuple b)
{
	return (
		fabs(a.x - b.x) < .0000001
		&& fabs(a.y - b.y) < .0000001
		&& fabs(a.z - b.z) < .0000001
		&& fabs(a.w - b.w) < .0000001);
}
